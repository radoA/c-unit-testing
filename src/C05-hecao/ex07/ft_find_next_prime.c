int	is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb <= 3)
		return (nb > 1);
	while (i < nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	int	i;

	i = nb;
	while (i >= nb)
	{
		if (is_prime(i))
			return (i);
		i++;
	}
	return (0);
}
